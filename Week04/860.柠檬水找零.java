// @lc code=start
class Solution {
    public boolean lemonadeChange(int[] bills) {
        if (bills.length == 0){
            return false;
        }

        int five = 0, ten = 10;
        for (int bill : bills) {
            if (bill == 5){
                five++;
            } else if (bill == 10) {
                if (five == 0) {
                    return false;
                }
                five--;
                ten++;
            } else {
                if (five > 0 && ten > 10) {
                    five--;
                    ten--;
                } else if (five >= 3) {
                    five -= 3;
                } else {
                    return false;
                }
            }
        }
        
        return true;
    }
}
